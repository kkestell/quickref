# Arithmetic operators
x = 10 + 5   # 15
y = 10 - 5   # 5
z = 10 * 5   # 50
w = 10 / 5   # 2.0

# Comparison operators
x = 10 == 5  # False
y = 10 != 5  # True
z = 10 < 5   # False
w = 10 <= 5  # False

# Logical operators
x = True and False  # False
y = True or False   # True
z = not True        # False

