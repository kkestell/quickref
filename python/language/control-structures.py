# Conditional statements
if x > 10:
    print('x is greater than 10')
elif x < 10:
    print('x is less than 10')
else:
    print('x is equal to 10')

# Loops
for i in range(10):
    print(i)

while x < 10:
    x += 1
    print(x)

# Break and continue
for i in range(10):
    if i == 5:
        break
    print(i)

for i in range(10):
    if i == 5:
        continue
    print(i)
